#!/usr/bin/env ruby
# encoding: UTF-8

class Artist
  attr_accessor :name
  @@all = []

  def initialize(name)
    @name = name
  end

  def self.all
    @@all
  end

  def self.destroy_all
    @@all.clear
  end

  def save
    @@all << self
    self
  end

  def self.create(artist)
    Artist.new(artist).save
  end
end

